def random_rotation_matrix(size, device):
    # return torch.eye(size).to(device)
    A = torch.randn(size, size)
    R, _ = torch.linalg.qr(A)
    det = torch.det(R)
    if det < 0:
        R[:, 0] *= -1
    return R.to(device)


def random_hadamard_matrix(size, device) -> torch.Tensor:
    if (
        (size % 20 == 0 and is_pow2(size // 20))
        or (size % 60 == 0 and is_pow2(size // 60))
        or is_pow2(size)
    ):
        Q = torch.randint(low=0, high=2, size=(size,)).to(torch.float)
        Q = Q * 2 - 1
        Q = torch.diag(Q)
        result = matmul_hadU(Q).to(device).detach()
    else:
        print(f"Hadamard_{size} is not implemented. Return identity matrix.")
        result = torch.eye(size).to(device).detach()
    return result


def matmul_hadU(X, transpose=False):
    n = X.shape[-1]
    hadK, K = get_hadK(n, transpose)
    input = X.clone().view(-1, n, 1)
    output = input.clone()
    while input.shape[1] > K:
        input = input.view(input.shape[0], input.shape[1] // 2, 2, input.shape[2])
        output = output.view(input.shape)
        output[:, :, 0, :] = input[:, :, 0, :] + input[:, :, 1, :]
        output[:, :, 1, :] = input[:, :, 0, :] - input[:, :, 1, :]
        output = output.view(input.shape[0], input.shape[1], -1)
        (input, output) = (output, input)
    del output

    if K > 1:
        # Do not explicitly repeat - OOM
        # input = torch.bmm(
        #     hadK.repeat(len(input), 1, 1).to(input.device).to(input.dtype), input)
        # Use bcast instead
        input = hadK.view(1, K, K).to(input) @ input

    return input.view(X.shape) / torch.tensor(n).sqrt()


def get_hadK(n, transpose=False):
    hadK, K = None, None
    if n % 60 == 0 and is_pow2(n // 60):
        K = 60
        hadK = get_had60().T if transpose else get_had60()
    elif n % 20 == 0 and is_pow2(n // 20):
        K = 20
        hadK = get_had20().T if transpose else get_had20()
    else:
        assert is_pow2(n), f"{n} Hadamard is not implement"
        K = 1
    return hadK, K


def is_pow2(n):
    return (n & (n - 1) == 0) and (n > 0)


